#include "librt.h"

void	matrix4_4_mult_matrix(t_matrix4_4 m1, t_matrix4_4 m2, t_matrix4_4 r)
{
	int	i;
	int	j;

	i = -1;
	while (++i < 4)
	{
		j = -1;
		while (++j < 4)
			r[i][j] = m1[i][0] * m2[0][j] + m1[i][1] * m2[1][j] + m1[i][2] *
						m2[2][j] + m1[i][3] * m2[3][j];
	}
}
