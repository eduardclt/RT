#include "col_prototype.h"

t_colour	col_add(t_colour c1, t_colour c2)
{
	t_colour ret;

	ret.r = c1.r + c2.r;
	ret.g = c1.g + c2.g;
	ret.b = c1.b + c2.b;
	return (ret);
}
